// Lookup: try cueKey first, then fuzzy match exercise name
export function getExerciseInstruction(exerciseName, cueKey) {
  if (cueKey && EXERCISE_INSTRUCTIONS_HE[cueKey]) {
    return EXERCISE_INSTRUCTIONS_HE[cueKey];
  }
  // Fuzzy: try to find by Hebrew exercise name
  if (exerciseName) {
    const lower = exerciseName.toLowerCase();
    for (const [key, val] of Object.entries(EXERCISE_INSTRUCTIONS_HE)) {
      if (val.name && lower.includes(val.name.split(' ')[0].toLowerCase())) {
        return val;
      }
    }
  }
  return null;
}

